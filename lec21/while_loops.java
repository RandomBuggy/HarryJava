public class while_loops {
    public static void main(String[] args) {
        System.out.println(1);
        System.out.println(2);
        System.out.println(3);


        byte i = 1;
        while (i <= 3) {
            System.out.println(i);
            i++;
        }

        int I = 100;
        while (I <= 200) {
            System.out.println(I);
            I++;
        }
    }
}

