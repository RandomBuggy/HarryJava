public class if_else_conditionals {
    public static void main(String args[]) {
        int age = 26;
        age = 4;
        boolean condition = (age != 18);
        if (condition) {
            System.out.println("You can drive!!");
        }

        else {
            System.out.println("Oh! no, you can't!!!");
        }
    }
}
